package day7_shortHandRelation;

public class RelatinalOperators {

public static void main(String[] args) {
    System.out.println("<<<<< Relational Operators >>>>>");
        /*        Relation Operators are:
 Greater than
                >= Greater than  or  equal
                <  Less than
                <= Less than  or  equal
                == Equal
                != Not Equal
         */
    System.out.println("step1: " + (10 > 11));
    System.out.println("step2: " + (10 > 10));
    System.out.println("step3: " + (10 >= 10));
    System.out.println("step4: " + (10 <= 10));
    System.out.println("step5: " + (10 == 10));
    System.out.println("step6: " + (11 == 10));
    System.out.println("step7: " + (10 != 10));
    System.out.println("step8: " + (11 != 10));
    }
}

