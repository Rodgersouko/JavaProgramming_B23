package day28_methods;

public class CustomMethodIntro {

    public static void main(String[] args) {
        printHello5Times();
        System.out.println("Roja");
        printHello5Times();
    }
    public static void printHello5Times(){
        for (int i = 0; i < 5; i++) {
            System.out.println("Baby");
        }
    }
}
